void solve() {
	ll n;
	cin >> n;
	ll cells = 1, steps = 1;
	ll ans = 0;
	for(ll i=3; i<=n; i+=2) {
		ans += steps*(i*i - cells);
		cells = i*i;
		steps++;
	}
	cout << ans << endl;
}

